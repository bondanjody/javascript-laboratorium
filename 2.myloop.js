// Title = Loop berdasarkan jumlah karakter string suatu obyek
var nama = "Diahajeng Anggraini";

// Membuat perulangan for dengan batas perulangan adalah jumlah karakter variabel nama
for (i = 1; i <= nama.length; i++){
    console.log(i);
}

// Menampilkan informasi jumlah karakter dari variabel nama
console.log('Jumlah karakter dari ' + nama + ' adalah :' + nama.length);